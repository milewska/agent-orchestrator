export async function POST(request: Request) {
  try {
    const body = await request.json();
    const parsed = QuickStartProjectSchema.safeParse(body);
    if (!parsed.success) {
      return NextResponse.json(
        { error: parsed.error.issues[0]?.message ?? "Invalid quick start request" },
        { status: 400 },
      );
    }

    const projectId = sanitizeProjectId(parsed.data.name);
    if (!projectId) {
      return NextResponse.json({ error: "Name is required" }, { status: 400 });
    }

    const projectRoot = resolve(parsed.data.location);
    const targetDir = resolve(projectRoot, projectId);

    await ensureDirectory(projectRoot);
    await ensureMissingOrEmptyDirectory(targetDir);
    await ensureDirectory(targetDir);

    if (parsed.data.template === "nextjs") {
      await writeNextJsTemplate(targetDir, parsed.data.name.trim());
    } else {
      await writeEmptyTemplate(targetDir, parsed.data.name.trim());
    }

    const config = buildAoConfig(parsed.data.name.trim(), projectId, targetDir);
    await writeFile(join(targetDir, "agent-orchestrator.yaml"), `${configToYaml(config)}`, "utf-8");

    await execFileAsync("git", ["init", "-b", "main"], {
      cwd: targetDir,
      timeout: 30_000,
    }).catch(async () => {
      await execFileAsync("git", ["init"], {
        cwd: targetDir,
        timeout: 30_000,
      });
      await execFileAsync("git", ["branch", "-M", "main"], {
        cwd: targetDir,
        timeout: 30_000,
      });
    });

    const project = registerAndResolveProject(targetDir, {
      configProjectKey: projectId,
      displayName: parsed.data.name.trim(),
    });

    return NextResponse.json({
      project: {
        id: project.id,
        name: project.name,
        path: targetDir,
      },
    });
  } catch (err) {
    return NextResponse.json(
      { error: err instanceof Error ? err.message : "Failed to create project" },
      { status: 500 },
    );
  }
}
