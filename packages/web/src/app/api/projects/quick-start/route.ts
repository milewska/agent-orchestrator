import { execFile } from "node:child_process";
import { mkdir, stat, writeFile } from "node:fs/promises";
import { join, resolve } from "node:path";
import { promisify } from "node:util";
import { NextResponse } from "next/server";
import { configToYaml, generateSessionPrefix, sanitizeProjectId } from "@composio/ao-core";
import { QuickStartProjectSchema } from "@/lib/api-schemas";
import { registerAndResolveProject } from "@/lib/project-registration";

const execFileAsync = promisify(execFile);

async function ensureDirectory(path: string): Promise<void> {
  await mkdir(path, { recursive: true });
}

async function ensureMissingOrEmptyDirectory(path: string): Promise<void> {
  try {
    const info = await stat(path);
    if (!info.isDirectory()) {
      throw new Error(`Target path exists and is not a directory: ${path}`);
    }
    throw new Error(`Target directory already exists: ${path}`);
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code === "ENOENT") {
      return;
    }
    throw error;
  }
}

function buildAoConfig(projectName: string, projectId: string, targetDir: string) {
  return {
    port: 3000,
    defaults: {
      runtime: "tmux",
      agent: "claude-code",
      workspace: "worktree",
      notifiers: ["desktop"],
    },
    projects: {
      [projectId]: {
        name: projectName,
        path: targetDir,
        defaultBranch: "main",
        sessionPrefix: generateSessionPrefix(projectId),
      },
    },
  };
}

async function writeEmptyTemplate(targetDir: string, projectName: string): Promise<void> {
  await Promise.all([
    writeFile(
      join(targetDir, "README.md"),
      `# ${projectName}\n\nBootstrapped with Agent Orchestrator quick start.\n`,
      "utf-8",
    ),
    writeFile(join(targetDir, ".gitignore"), "node_modules\n.next\ndist\n.env\n", "utf-8"),
  ]);
}

